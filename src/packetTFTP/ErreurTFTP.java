public class ErreurTFTP extends Exception{
    private int errType;
    
    public ErreurTFTP(int type, String msg){
        super(msg);
        errType=type;
    }

    public int getErrType() {
        return errType;
    }

    public void setErrType(int errType) {
        this.errType = errType;
    }
}
